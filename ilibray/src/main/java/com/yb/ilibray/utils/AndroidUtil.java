    /**
     * 得到SDCard信息：数组中：[0]总大小 [1]可使用大小
     * Created by yb(yb498869020@hotmail.com) on 2015/11/28 21:24.
     */
    public static long[] getSDCardTotalSpace(){
        File path = Environment.getExternalStorageDirectory();
        StatFs stat = new StatFs(path.getPath());
        long blockSize = stat.getBlockSize();
        long totalBlocks = stat.getBlockCount();
        long availableBlocks = stat.getAvailableBlocks();

        long totalSize = totalBlocks * blockSize;
        long availSize = availableBlocks * blockSize;

//        String totalStr = Formatter.formatFileSize(BaseApplication.context(), totalSize);
//        String availStr = Formatter.formatFileSize(BaseApplication.context(), availSize);
        return new long[]{totalSize,availSize};
    }
    public static String getSystemProperty(String propName) {
        String line;
        BufferedReader input = null;
        try {
            Process p = Runtime.getRuntime().exec("getprop " + propName);
            input = new BufferedReader(new InputStreamReader(p.getInputStream()), 1024);
            line = input.readLine();
            input.close();
        } catch (IOException ex) {
            return null;
        } finally {
            if (input != null) {
                try {
                    input.close();
                } catch (IOException e) {
                }
            }
        }
        return line;
    }

    /**
     * 判断是否是小米V7和V8系统
     * @return
     */
    public static boolean muiuVersionV8(){
        return Build.MANUFACTURER.equals("Xiaomi") && (
                Check.checkReplace(getSystemProperty("ro.miui.ui.version.name")).equals("V8"));
    }
